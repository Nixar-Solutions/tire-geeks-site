export const getFeaturedBuilds = () => galleryBuilds.filter(b => b.featured).slice(0, 8);
export const getBuildsByCategory = (cat: string) => cat === 'all' ? galleryBuilds : galleryBuilds.filter(b => b.category === cat);
export const getBuildBySlug = (slug: string) => galleryBuilds.find(b => b.slug === slug);
export const getRelatedBuilds = (current: GalleryBuild, count = 4) => {
  const sameCategory = galleryBuilds.filter(b => b.id !== current.id && b.category === current.category);
  if (sameCategory.length >= count) return sameCategory.slice(0, count);
  const others = galleryBuilds.filter(b => b.id !== current.id && b.category !== current.category);
  return [...sameCategory, ...others].slice(0, count);
};
